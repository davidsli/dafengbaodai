package com.liyang.client.strategy;

/**
 * @author Administrator
 *
 */
public class SecurityInfoXiaoma implements ISecurityInfo {

	private String apiKey;
	private String token;

	public SecurityInfoXiaoma(String apikey) {
		this.apiKey = apikey;
	}

	/**
	 * @return the apiKey
	 */
	public String getApiKey() {
		return apiKey;
	}

	/**
	 * @param apiKey
	 *            the apiKey to set
	 */
	public void setApiKey(String apiKey) {
		this.apiKey = apiKey;
	}

	/**
	 * @return the token
	 */
	@Override
	public String getToken() {
		return token;
	}

	/**
	 * @param token
	 *            the token to set
	 */
	public void setToken(String token) {
		this.token = token;
	}

}
