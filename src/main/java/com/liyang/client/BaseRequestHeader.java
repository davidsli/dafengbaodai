package com.liyang.client;
/**
 * 
 * @author Administrator
 *
 */
public class BaseRequestHeader {

	public BaseRequestHeader() {

	}

	public BaseRequestHeader(String openID, String sign, String token, String tradeType, String tradeNo,
			String tradeDate) {
		this.openID = openID;
		this.sign = sign;
		this.token = token;
		this.tradeType = tradeType;
		this.tradeNo = tradeNo;
		this.tradeDate = tradeDate;
	}

	private String openID;

	/**
	 * 用户标识
	 * 
	 * @return the openID
	 */
	public String getOpenID() {
		return openID;
	}

	/**
	 * 用户标识
	 * 
	 * @param openID
	 *            the openID to set
	 */
	public void setOpenID(String openID) {
		this.openID = openID;
	}

	private String sign;

	/**
	 * 签名
	 * 
	 * @return the sign
	 */
	public String getSign() {
		return sign;
	}

	/**
	 * 签名
	 * 
	 * @param sign
	 *            the sign to set
	 */
	public void setSign(String sign) {
		this.sign = sign;
	}

	private String token;

	/**
	 * 验签密钥
	 * 
	 * @return the token
	 */
	public String getToken() {
		return token;
	}

	/**
	 * 验签密钥
	 * 
	 * @param token
	 *            the token to set
	 */
	public void setToken(String token) {
		this.token = token;
	}

	private String tradeType;

	/**
	 * 交易类型
	 * 
	 * @return the tradeType
	 */
	public String getTradeType() {
		return tradeType;
	}

	/**
	 * 交易类型
	 * 
	 * @param tradeType
	 *            the tradeType to set
	 */
	public void setTradeType(String tradeType) {
		this.tradeType = tradeType;
	}

	private String tradeNo;

	/**
	 * 交易流水号
	 * 
	 * @return the tradeNo
	 */
	public String getTradeNo() {
		return tradeNo;
	}

	/**
	 * 交易流水号
	 * 
	 * @param tradeNo
	 *            the tradeNo to set
	 */
	public void setTradeNo(String tradeNo) {
		this.tradeNo = tradeNo;
	}

	private String tradeDate;

	/**
	 * 交易时间
	 * 
	 * @return the tradeDate
	 */
	public String getTradeDate() {
		return tradeDate;
	}

	/**
	 * 交易时间
	 * 
	 * @param tradeDate
	 *            the tradeDate to set
	 */
	public void setTradeDate(String tradeDate) {
		this.tradeDate = tradeDate;
	}
}
