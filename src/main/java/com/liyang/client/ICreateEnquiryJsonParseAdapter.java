package com.liyang.client;

/**
 * 从询价请求的json字符串中提取出需要的内容
 * 
 * @author huanghengkun
 * @create 2017年12月1日
 */
public interface ICreateEnquiryJsonParseAdapter {

	/**
	 * 解析出车牌号码
	 * 
	 * @return
	 */
	Object getLicenseNumber();

	/**
	 * 解析出车主姓名
	 * 
	 * @return
	 */
	Object getOwnerName();

	/**
	 * 解析出保险公司第一个方案名称
	 * 
	 * @return
	 */
	Object getInsurancesListSchemeName();

	/**
	 * 解析出城市名称
	 * 
	 * @return
	 */
	Object getCityName();

	/**
	 * 解析出交强险起保日期
	 * 
	 * @return
	 */
	Object getForceInsuranceStartTime();

	/**
	 * 解析出商业险起保日期
	 * 
	 * @return
	 */
	Object getInsuranceStartTime();
	
	/**
	 * 解析出保险公司编号列表，格式1,2,3
	 * 
	 * @return
	 */
	Object getInsuranceCompanyName();

	/**
	 * 解析出身份证号
	 * 
	 * @return
	 */
	Object getIdCard();
}
