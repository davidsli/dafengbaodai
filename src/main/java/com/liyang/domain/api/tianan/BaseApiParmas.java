package com.liyang.domain.api.tianan;

/**
 * 模拟类型
 * @author Administrator
 *
 */
public abstract class BaseApiParmas implements IApiParams{

	/**
	 * 模拟类型，可空
	 * <p>空则使用正常接口
	 */
	protected String mockType;

	@Override
	public String getMockType() {
		return mockType;
	}

	@Override
	public void setMockType(String mockType) {
		this.mockType = mockType;
	}
	
	
}
