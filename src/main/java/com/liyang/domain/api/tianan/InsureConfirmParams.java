package com.liyang.domain.api.tianan;


/**
 * 投保单保存，需要传入的参数
 * @author Administrator
 *
 */
public class InsureConfirmParams extends BaseApiParmas implements IApiParams{
	//投保人id，查询投保人相关信息
	private String applyId;
	//被保人id，查询被保人相关信息
	private String insuredId;
	//车主id，查询车主相关信息
	private String carOwnerId;
	//产品类型
	private String rateMark;
	private String tradeNo;
	public String getTradeNo() {
		return tradeNo;
	}
	public void setTradeNo(String tradeNo) {
		this.tradeNo = tradeNo;
	}
	public String getApplyId() {
		return applyId;
	}
	public void setApplyId(String applyId) {
		this.applyId = applyId;
	}
	public String getInsuredId() {
		return insuredId;
	}
	public void setInsuredId(String insuredId) {
		this.insuredId = insuredId;
	}
	public String getCarOwnerId() {
		return carOwnerId;
	}
	public void setCarOwnerId(String carOwnerId) {
		this.carOwnerId = carOwnerId;
	}
	public String getRateMark() {
		return rateMark;
	}
	public void setRateMark(String rateMark) {
		this.rateMark = rateMark;
	}
	
	
}
