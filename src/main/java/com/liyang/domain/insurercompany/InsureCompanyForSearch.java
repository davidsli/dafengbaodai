package com.liyang.domain.insurercompany;

/**
 * 关键字搜索保险公司
 * @author Administrator
 *
 */
public class InsureCompanyForSearch {
	private Integer insurerCompanyId;
	private String name;
	private String stateCode;
	
	public Integer getInsurerCompanyId() {
		return insurerCompanyId;
	}
	public void setInsurerCompanyId(Integer insurerCompanyId) {
		this.insurerCompanyId = insurerCompanyId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStateCode() {
		return stateCode;
	}
	public void setStateCode(String stateCode) {
		this.stateCode = stateCode;
	}
	
	
	
}
